def order(drinks, num):
    price = 0
    if drinks == "coffee":
        price = 1.50 * num
    elif drinks == "coke":
        price = 1.40 * num
    elif drinks == "water":
        price = 1.00 * num
    elif drinks == "snacks":
        price = 2.00 * num
    return f"{price:.2F}"


drink = input()
quantity = int(input())
print(order(drink, quantity))
